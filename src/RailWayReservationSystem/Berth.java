package RailWayReservationSystem;
public class Berth {
    private int lower;
    private int upper;
    private int midUpper;
    private int sideLower;
    private int sideUpper;

    public int getLower() {
        return lower;
    }

    public void setLower(int lower) {
        this.lower = lower;
    }

    public int getUpper() {
        return upper;
    }

    public void setUpper(int upper) {
        this.upper = upper;
    }

    public int getMidUpper() {
        return midUpper;
    }

    public void setMidUpper(int midUpper) {
        this.midUpper = midUpper;
    }

    public int getSideLower() {
        return sideLower;
    }

    public void setSideLower(int sideLower) {
        this.sideLower = sideLower;
    }

    public int getSideUpper() {
        return sideUpper;
    }

    public void setSideUpper(int sideUpper) {
        this.sideUpper = sideUpper;
    }
}
